    private void resolverSudocraftActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resolverSudocraftActionPerformed
        //verificar si la solucion que nos manda el usuario
        //si hay ceros mandar a solucionar
        //si no hay errores mandar a solucionar
        int band=0;// para ver si no hay errores
        int bandLleno=0;//para ver si el arreglo solo necesita revision
        int k;
        
        int[][] sol=tomarArreglo();
        for(int g=0;g<9;g++){
            for(int f=0;f<9;f++){
                if(sol[g][f]!=0){
                    k=sol[g][f];
                    sol[g][f]=0;
                    if(!s.verificar(sol,k,g,f)){
                        death();
                        JOptionPane.showMessageDialog(null, "Error encontrado en fila: " + (g+1) +", columna: "+ (f+1) );
                        band=1;
                        break;
                    }
                    else
                        sol[g][f]=k;
                }
                else{
                    bandLleno=1;
                }
            }
                if(band==1)break;
        } 
        if(band==0&&bandLleno==1){//ningun error y hay algun vacio para resolver
            s.resuelve();
        }
        else    if (band==0&&bandLleno==0)//ningun error y ningun vacio
                    {
                    nivel();
                    JOptionPane.showMessageDialog(null, "FELICITACIONES! LO HAS HECHO BIEN");
                   
                    }
                else {
                    cmdlimpiar.doClick();
                    
                    }
    }//GEN-LAST:event_resolverSudocraftActionPerformed

